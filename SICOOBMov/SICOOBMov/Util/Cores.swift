extension UIColor{
        
    static func Principal() -> UIColor{ //verde escuro
        return UIColor(red: 0.03, green: 0.21, blue: 0.26, alpha: 1.00)
    }
    
    static func Secundaria() -> UIColor{ //verde claro
        return UIColor(red: 0.15, green: 0.68, blue: 0.62, alpha: 1.00)
    }
    
    static func FontePrimaria() -> UIColor{ //verde medio
        return UIColor(red: 0.46, green: 0.71, blue: 0.19, alpha: 1.00)
    }
    
    static func FonteSecundaria() -> UIColor{ //amarelo
        return UIColor(red: 0.75, green: 0.84, blue: 0.19, alpha: 1.00)
    }
    
    static func Branco() -> UIColor{ //branco
        return UIColor(red: 0.92, green: 0.93, blue: 0.94, alpha: 1.00)
    }
}
